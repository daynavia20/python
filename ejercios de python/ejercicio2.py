#leer un numero entero y determinar si tiene 3 digitos 
num=int(input('digite un numero: '))# se pide el digito a ingresar 
if (num>99 and num<1000):#en este caso se coloca si el numero es mayor de 99 y si es menorde 1000 
    print('el numero tiene 3 digitos') # si eso pasa entonces le decimos que si tiene 3 digitos 
else:
    print('el numero no tiene 3 digitos ')# si no es el caos entonces le decimos que no tiene esos 3 numeros 
